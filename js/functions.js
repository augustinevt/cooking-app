//Celsius to Fahrenheit
function c2fTemp(celsiusTemp) {
  return celsiusTemp * 1.8 + 32;
}


//Fahrenheit to Celsius
function f2cTemp(fahrenheitTemp) {
  return  (fahrenheitTemp - 32) / 1.8;
}

